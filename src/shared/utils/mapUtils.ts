export const mapUtils = {
  /**
   * Перетворює Map у масив пар ключ-значення.
   *
   * Корисно для:
   * - серіалізації (наприклад, під JSON)
   * - використання методів масивів (map, filter, reduce)
   * - зручної функціональної ітерації
   *
   * @template K - тип ключів
   * @template V - тип значень
   * @param {Map<K, V>} map - Map, яку потрібно перетворити
   * @returns {[K, V][]} Масив пар [ключ, значення]
   */
  toArray: <K, V>(map: Map<K, V>): [K, V][] => {
    return Array.from(map.entries());
  },

  /**
   * Перетворює масив пар ключ-значення у Map.
   *
   * Корисно для:
   * - відновлення Map після JSON-десеріалізації
   * - створення швидких структур для пошуку по ключу
   * - відновлення стану з простих даних
   *
   * @template K - тип ключів
   * @template V - тип значень
   * @param {[K, V][]} array - масив пар [ключ, значення]
   * @returns {Map<K, V>} відновлений Map
   */
  toMap: <K, V>(array: [K, V][]): Map<K, V> => {
    return new Map(array);
  },
};